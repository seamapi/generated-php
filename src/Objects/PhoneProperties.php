        public PhoneMinutMetadata | null $minut_metadata,
        public PhoneFourSuitesMetadata | null $four_suites_metadata,
        public PhoneTwoNMetadata | null $two_n_metadata,
        public PhoneControlbywebMetadata | null $controlbyweb_metadata,
        public PhoneTtlockMetadata | null $ttlock_metadata,
        public PhoneSeamBridgeMetadata | null $seam_bridge_metadata,
